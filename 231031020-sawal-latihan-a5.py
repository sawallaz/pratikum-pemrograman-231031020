z = 0
while z < 5:
    print('menjalankan program!')
    z+=1


'''
pembahasan baris-perbaris:

1. 'z = 0': Di sini, kita mendefinisikan variabel 'z' dan menginisialisasinya dengan nilai 0.

2. 'while z < 5:': Ini adalah pernyataan loop 'while'. Ini berarti "selama kondisi 'z < 5' terpenuhi, lakukan hal berikutnya di dalam loop."

3. 'print('menjalankan program!')': Baris ini mencetak teks 'menjalankan program!' ke layar setiap kali iterasi loop dijalankan.

4. 'z += 1': Ini adalah operasi yang menambahkan 1 ke nilai 'z' setiap kali iterasi loop selesai. Jadi, nilai 'z' akan bertambah seiring berjalannya loop.

jalankan kode langkah per langkah:
- Awalnya, 'z' sama dengan 0.
- Kita masuk ke dalam loop 'while'. Karena nilai 'z' saat ini adalah 0 (kurang dari 5), kita akan masuk ke dalam loop.
- Di dalam loop, kita mencetak 'menjalankan program!' ke layar.
- Selanjutnya, kita menambahkan 1 ke nilai 'z', sehingga 'z' sekarang bernilai 1.
- Kita kembali ke atas loop, karena nilai 'z' (1) masih kurang dari 5.
- Proses ini terus berlanjut sampai nilai 'z' menjadi 5.
- Ketika 'z' menjadi 5, kondisi 'z < 5' tidak terpenuhi lagi, dan kita keluar dari loop.

Jadi, hasil dari kode ini adalah mencetak 'menjalankan program!' ke layar sebanyak 5 kali, karena kita memulai dari 0 dan menambahkannya satu per satu hingga mencapai 5.'''